const category = 'Tabs';

const stories = [
  'Tabs, isolated',
  'Tabs, in context',
  'Tabs (alt)',
  'Tabs, using a select',
];

const viewports = Cypress.config('viewports');

describe(category, () => {
  stories.forEach((story) => {
    viewports.forEach((vp) => {
      it(`${story} matches ${vp} snapshot`, () => {
        const params = {
          url: category,
          category,
          story,
          vp,
        };

        cy.visitAndCheckSnapshot(params);
      });
    });
  });
});
